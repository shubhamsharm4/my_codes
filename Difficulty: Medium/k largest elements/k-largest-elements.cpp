class Solution {
  public:
    vector<int> kLargest(vector<int>& arr, int k) {
        vector<int> res;
        priority_queue<int, vector<int>> pq;
        //I think it'll store teh greater element on top (descending order)
        for(int i : arr) pq.push(i);
        while(k > 0) {
            res.push_back(pq.top());
            pq.pop();
            k--;
        }
        
        return res; 
    }
};

